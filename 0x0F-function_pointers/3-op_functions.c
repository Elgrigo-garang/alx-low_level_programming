#include "3-calc.h"
/**
 * op_add-calculates sum of integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of a and b
 */
int op_add(int a, int b)
	{
		return (a + b);
	}
/**
 * op_sub-calculates difference of two integers
 * @a : first integer
 * @b : second integer
 *
 * Return: difference of a and b
 */
int op_sub(int a, int b)
	{
		return (a - b);
	}
/**
 * op_mul-calculates the products of two itegers
 * @a : first integer
 * @b : second integer
 *
 * Return: product of two integers
 */
int op_mul(int a, int b)
	{
		return (a * b);
	}
/**
 * op_div-calculates division of two integers
 * @a :first integer
 * @b:second integer
 *
 * Return:the results of division of a and b
 */
int op_div(int a, int b)
	{
		return (a / b);
	}
/**
 * op_mol- calculates the remender of two integer after division
 * @a: first integer
 * @b: second integer
 *
 * Return:the remender of a and b
 */
int op_mol(int a, int b)
	{
		return (a % b);
	}
